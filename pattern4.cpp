  #include<iostream>
  
  using namespace std;
  
  int main ()  {
  	
  int n;
  
  cin>>n;
   
   int i=1;
   
   while(i<=n){
   	
   	// space print 
   	
   	int space = n-i;
   	while(space){
   		cout<<" ";
   		
   		space=space-1;
	   }
    // printing first triangle
    
   	int j=1;
   	while(j<=i){
   	    cout<<j;
   		j++;
	   }
	   
	   // printing 2nd tri angle
	   
	int start = i-1;
	while(start){
	cout<<start;
	start--;
	}
	   cout<<endl;
	   i++;
   }
	  
  }

  
  
